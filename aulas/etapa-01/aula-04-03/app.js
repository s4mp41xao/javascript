const age = 31

// igual a, e diferente de
// console.log(age == 31)
// console.log(age =='31')
// console.log(age != 31)
// console.log(age != '31')


// === (estritamente igual a): Verifica se dois valores são iguais e têm o mesmo tipo de dados. Por exemplo: '5' === 5 retorna false, porque os tipos são diferentes.
console.log(age === 31)
console.log(age === '31')

//  !== (estritamente diferente de): Verifica se dois valores não são iguais e/ou têm tipos de dados diferentes. Por exemplo: '5' !== 5 retorna true, porque os tipos são diferentes.
console.log(age !== 31)
console.log(age !== '31')
