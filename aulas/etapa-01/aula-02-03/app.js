// Exibindo a frase "hello, world" no console
console.log('hello, world')

// Exibindo o valor da variável "email" no console
const email = 'leon@rogermelo.com.br'
console.log(email)

// Concatenando strings para formar o nome completo
const firstName = 'Nathan'
const lastName = 'Drake'
const fullName = firstName + ' ' + lastName

// Exibindo o nome completo no console
console.log(fullName)

// Acessando e convertendo um caractere específico em maiúsculo
console.log(fullName[2].toUpperCase())

// Obtendo o comprimento (quantidade de caracteres) da string
console.log(fullName.length)

// Utilizando o método toUpperCase() para converter a string para letras maiúsculas
console.log(fullName.toUpperCase())

// Atribuindo o resultado do método toLowerCase() a uma nova variável "result"
const result = fullName.toLowerCase()

// Exibindo o valor das variáveis "result" e "fullName" no console
console.log(result, fullName)

// Encontrando o índice do caractere '@' na string "email"
const index = email.indexOf('@')

// Exibindo o valor do índice no console
console.log(index)
